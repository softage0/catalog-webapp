def create_user(login_session):
    user = User(name=login_session['username'], email=login_session['email'], picture=login_session['picture'])
    session.add(user)
    session.commit()
    user = session.query(User).filter_by(email=login_session['email']).one()
    return user.id


def get_user_info(user_id):
    user = session.query(User).filter_by(id=user_id).one()
    return user


def get_user_id(email):
    try:
        user = session.query(User).filter_by(email=email).one()
        return user.id
    except:
        return None


@app.route('/login')
def show_login():
    """ Create anti-forgery state token """
    state = ''.join(random.choice(string.ascii_uppercase + string.digits) for _ in xrange(32))
    login_session['state'] = state
    categories = session.query(Category).all()
    return render_template("login.html", STATE=state, config=config, categories=categories)


@app.route('/disconnect')
def disconnect():
    """ Disconnect based on provider """
    if 'provider' in login_session:
        if login_session['provider'] == 'google':
            gdisconnect()
            del login_session['gplus_id']
        if login_session['provider'] == 'facebook':
            fbdisconnect()
            del login_session['facebook_id']
        del login_session['username']
        del login_session['email']
        del login_session['picture']
        del login_session['user_id']
        del login_session['provider']
        flash("You have successfully been logged out.")
        return redirect(url_for('catalog_main'))
    else:
        flash("You were not logged in")
        return redirect(url_for('catalog_main'))


